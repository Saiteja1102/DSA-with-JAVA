/*
 Relational Operators:
 1. == -> equal
 2. != -> not equal to
 3. > -> Greater than
 4. < -> Less than
 5. >= -> Greater than equal to
 6. <= -> Less than equal to
 */

public class D03_Relational_Operators {
    public static void main(String[] args) {
        // Relational Operators
        int A = 10;
        int B = 5;
        System.out.println("A == B : " + (A==B));
        System.out.println("A != B : " + (A!=B));
        System.out.println("A > B : " + (A>B));
        System.out.println("A < B : " + (A<B));
        System.out.println("A >= B : " + (A>=B));
        System.out.println("A <= B : " + (A<=B));
    }
}
