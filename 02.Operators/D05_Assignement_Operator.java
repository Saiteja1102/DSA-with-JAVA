// Assignement Operators

public class D05_Assignement_Operator {
    public static void main(String[] args){
        // Assignement Operator
        // =
        int A = 10;
        // +=
        A += 10; // A = A + 10
        // This makes code run faster this helps in competitive programming
        System.out.println(A);
        A -= 3; // A = A - 3
        System.out.println(A);
        A *= 7; // A = A * 7
        System.out.println(A);
        A /= 5; // A = A / 5
        System.out.println(A);
        A %= 4; // A = A % 4
        System.out.println(A);
        
    }
}
