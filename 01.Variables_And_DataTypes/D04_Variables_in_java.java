// Variables in java

public class D04_Variables_in_java {
    public static void main(String[] args){
        int a = 10; // a -> is a variable
        int b = 20; // b -> is also a variable
        System.out.println(a);
        System.out.println(b);

        // For Strings
        String name = "Sai Teja"; // name -> variable
        System.out.println(name);

        // Values of variables can be changed
        a = 50;
        System.out.println(a);
        // One variable value can be assigned to another
        b = a;
        System.out.println(a+" "+b);
    }
}
