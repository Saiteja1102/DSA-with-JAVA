public class D02_output_in_java {
    public static void main(String[] args){
        // 1. print
        System.out.println("------------1. print---------------");
        System.out.print("Hello World!!"); // ; -> Statement Terminator
        System.out.print("Hello World!!");

        // 2. println
        System.out.println("\n------------2. println---------------");
        System.out.println("Hello World!!"); // println -> will go to new line at the end
        System.out.println("Hello World!!");

        // 3. \n
        System.out.println("------------2. \\n---------------");
        System.out.print("Hello World!!\nHello World!!\nHelloWorld!!"); // \n -> Next Line
    }
}
