// Hello World 100 times using while Loop

public class D01_whileLoop {
    public static void main(String[] args){
        int counter = 0;
        while(counter < 100){
            System.out.println("Hello World");
            counter++;
        }

        System.out.println("Printed Hello World 100 times..");
    }
}
