/*
        Do while
    do{
        // do something

    }while(condition)


    Note: Code will exceute atleast once..
 */
public class D09_do_while {
    public static void main(String[] args){
        int counter = 0;
        do{
            System.out.println("Hello World");
            counter++;
        }while(counter < 10);
    }
}
