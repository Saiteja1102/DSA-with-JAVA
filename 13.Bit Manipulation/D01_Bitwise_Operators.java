/*
Bit wise Operators
    1. Bitwise AND &
    2. Bitwise OR |
    3. Bitwise XOR ^
    4. Binary One's Compliment ~
    5. Binary Left Shift <<
    6. Binary Right Shift >>
 */

public class D01_Bitwise_Operators {
    public static void main(String[] args) {
        // 1. Bitwise AND &
        System.out.println("Bitwise AND & : "+(5 & 6));
        // 2. Bitwise OR |
        System.out.println("Bitwise OR | : "+(5 | 6));
        // 3. Bitwise XOR ^
        System.out.println("Bitwise XOR ^ :"+(5 ^ 2));
        // 4. Binary One's Compliment ~
        System.out.println("Binary One's Compliment ~ : "+~5);
        // 5. Binary Left Shift <<
        System.out.println("Binary Left Shift << : "+(5 << 2));
        // 6. Binary Right Shift >>
        System.out.println("Binary Right Shift >> : "+(6 >> 1));
    }
}
