// Function/Methods
/*
  Methods - Function written inside class is called as Method
                    Function Syntax
        returnType name(){
            // body
            return statement;
        }
 */

public class D01_FunctionSyntax {
    public static void printHelloWorld(){
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
    }
    public static void main(String[] args){
        printHelloWorld(); // Function Call
    }
}
