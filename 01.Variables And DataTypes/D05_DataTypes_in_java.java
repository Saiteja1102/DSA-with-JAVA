// Data Types in java
/* 
    1. byte - 1 byte
    2. char - 2 byte
    3. boolean - 1 byte
    4. short - 2 byte
    5. int - 4 byte
    6. long - 8 byte
    7. float - 4 byte
    8. double - 8 byte
 */

public class D05_DataTypes_in_java {
    public static void main(String[] args){
        // Datatypes
        byte a1 = 11;
        System.out.println(a1);
        char a2 = 'a';
        System.out.println(a2);
        boolean a3 = true;
        System.out.println(a3);
        short a4 = 124;
        System.out.println(a4);
        int a5 = 12345;
        System.out.println(a5);
        long a6 = 123456789;
        System.out.println(a6);
        float a7 = 123.123f;
        System.out.println(a7);
        double a8 = 12345678.12345;
        System.out.println(a8);
    }
}
